SELECT COUNT(OBJECTID) AS total_fires,
COUNT(DISTINCT CASE WHEN FIRE_SIZE_CLASS = 'A' THEN OBJECTID ELSE NULL END) AS a_size,
ROUND(COUNT(DISTINCT CASE WHEN FIRE_SIZE_CLASS = 'A' THEN OBJECTID ELSE NULL END) * 100.0 / COUNT(OBJECTID),1) AS a_pct,
COUNT(DISTINCT CASE WHEN FIRE_SIZE_CLASS = 'B' THEN OBJECTID ELSE NULL END) AS b_size,
ROUND(COUNT(DISTINCT CASE WHEN FIRE_SIZE_CLASS = 'B' THEN OBJECTID ELSE NULL END) * 100.0 / COUNT(OBJECTID),1) AS b_pct,
COUNT(DISTINCT CASE WHEN FIRE_SIZE_CLASS = 'C' THEN OBJECTID ELSE NULL END) AS c_size,
ROUND(COUNT(DISTINCT CASE WHEN FIRE_SIZE_CLASS = 'C' THEN OBJECTID ELSE NULL END) * 100.0 / COUNT(OBJECTID),1) AS c_pct,
COUNT(DISTINCT CASE WHEN FIRE_SIZE_CLASS = 'D' THEN OBJECTID ELSE NULL END) AS d_size,
ROUND(COUNT(DISTINCT CASE WHEN FIRE_SIZE_CLASS = 'D' THEN OBJECTID ELSE NULL END) * 100.0 / COUNT(OBJECTID),1) AS d_pct,
COUNT(DISTINCT CASE WHEN FIRE_SIZE_CLASS = 'E' THEN OBJECTID ELSE NULL END) AS e_size,
ROUND(COUNT(DISTINCT CASE WHEN FIRE_SIZE_CLASS = 'E' THEN OBJECTID ELSE NULL END) * 100.0 / COUNT(OBJECTID),1) AS e_pct,
COUNT(DISTINCT CASE WHEN FIRE_SIZE_CLASS = 'F' THEN OBJECTID ELSE NULL END) AS f_size,
ROUND(COUNT(DISTINCT CASE WHEN FIRE_SIZE_CLASS = 'F' THEN OBJECTID ELSE NULL END) * 100.0 / COUNT(OBJECTID),1) AS f_pct,
COUNT(DISTINCT CASE WHEN FIRE_SIZE_CLASS = 'G' THEN OBJECTID ELSE NULL END) AS g_size,
ROUND(COUNT(DISTINCT CASE WHEN FIRE_SIZE_CLASS = 'G' THEN OBJECTID ELSE NULL END) * 100.0 / COUNT(OBJECTID),1) AS g_pct
       FROM Fires